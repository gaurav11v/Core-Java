
public class SwappingNum {
	public static void main(String[] args) {
		int N1 = 50;
		int N2 = 100;
		System.out.println("N1=" +N1);
		System.out.println("N2=" +N2);
		
		System.out.println();
		
		int temp=N1;
		N1=N2;
		N2=temp;
		
		System.out.println("N1=" +N1);
		System.out.println("N2=" +N2);
	}


}
